#! /bin/sh
# Muq-config.sh
########################################################
# This file is intended only to be 'source'd by        #
# other shell scripts, not to be directly executed     #
# itself:  On some systems (e.g. ultrix 4.4),          #
# all shell scripts should end with "exit 0", but      #
# to be sourced, a file must obviously _not_ end       #
# with "exit 0".  Hence, we create disjoint classes    #
# of files.                                            #
########################################################

# Some central directory layout definitions
# sourced by other shell scripts.  These should
# all work out of the box, but you may choose to
# re-arrange things...

# The canonical arrangment is something like
#  muqhome:  $HOME/muq
#   bindir:  $HOME/muq/bin      -- executable images and scripts
#     cdir:  $HOME/muq/c        -- C source, Makefiles &tc
#     hdir:  $HOME/muq/h        -- C header files
#    dbdir:  $HOME/muq/db       -- files for running game
#   docdir:  $HOME/muq/doc      -- manuals
#   pkgdir:  $HOME/muq/pkg      -- standard Muq libraries
#   pubdir:  $HOME/muq/pub      -- exported files
#   srvdir:  $HOME/muq/srv      -- coprocess servers
#  htmldir:  $HOME/public_hmtl  -- where INSTALLED html docs live.
#   autoconfigdir: /usr/lib/autoconf -- home of gnu autoconfig pkg
# The rest of this file simply attempts to deduce
# reasonable values for the above -- you can replace
# it all with hardwired defines like
#   cdir=$HOME/muq/c
#   hdir=$HOME/muqc
#   ...
# if you prefer:

bindir=${MUQDIR-${HOME}/muq/bin}

# Assume bin is immediately under main muq dir,
# and deduce therefore that main muq dir is:
muqhome=`echo $bindir | sed -e 's|\(/.*\)/[^/]*|\1|'`

# Assume other directories are siblings to us:
cdir=$muqhome/c
hdir=$muqhome/h
dbdir=$muqhome/db
pkgdir=$muqhome/pkg
pubdir=$muqhome/pub
srvdir=$muqhome/srv
docdir=$muqhome/doc
htmldir=$HOME/public_html

# Locate autoconf dir.  Should move this
# logic to muq/c/Configure someday:
if [ -d /usr/lib/autoconf ]
then
    autoconfigdir=/usr/lib/autoconf
else
    if [ -d /usr/share/autoconf ]
    then
        autoconfigdir=/usr/share/autoconf
    else
        if [ -d /usr/local/lib/autoconf ]
	then
            autoconfigdir=/usr/local/lib/autoconf
	fi
    fi
fi

# Should check that they all exist likely, and go search
# for them if they don't... someday :).


# List of all applications currently generated by core distribution:
apps="muq z-info muq-import muq-export"

